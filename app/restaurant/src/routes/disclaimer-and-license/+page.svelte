	<h2>ข้อตกลงการใช้ซอร์ฟแวร์และข้อปฏิเสธความรับผิดชอบ</h2>
	<p>
		ซอร์ฟแวร์นี้เป็นผลงานที่พัฒนาขึ้นโดย นายพัสกร ยืนยง และนางสาวช่อเพชร ธนามีชัย จาก
		โรงเรียนวิทยาศาสตร์จุฬาภรณราชวิทยาลัย บุรีรัมย์ ภายใต้การดูแลของ นายเอกชัย วัฒนไชย
		ภายใต้โครงการบอตร้านป้า: แชตบอตรับออเดอร์ร้านอาหารตามสั่งโดยใช้ภาษาธรรมชาติ ซึ่งสนับสนุนโดย
		สำนักงานพัฒนาวิทยาศาสตร์และเทคโนโลยีแห่งชาติ
		โดยมีวัตถุประสงค์เพื่อส่งเสริมให้นักเรียนและนักศึกษาได้เรียนรู้และฝึกทักษะในการพัฒนาซอฟต์แวร์
		ลิขสิทธิ์ของซอฟต์แวร์นี้จึงเป็นของผู้พัฒนา
		ซึ่งผู้พัฒนาได้อนุญาตให้สำนักงานพัฒนาวิทยาศาสตร์และเทคโนโลยีแห่งชาติ เผยแพร่ซอฟต์แวร์นี้ตาม
		“ต้นฉบับ” โดยไม่มีการแก้ไขดัดแปลงใด ๆ ทั้งสิ้น
		ให้แก่บุคคลทั่วไปได้ใช้เพื่อประโยชน์ส่วนบุคคลหรือประโยชน์ทางการศึกษาที่ไม่มีวัตถุประสงค์ในเชิงพาณิชย์
		โดยไม่คิดค่าตอบแทนการใช้ซอฟต์แวร์ ดังนั้น สำนักงานพัฒนาวิทยาศาสตร์และเทคโนโลยีแห่งชาติ
		จึงไม่มีหน้าที่ในการดูแล บำรุงรักษา จัดการอบรมการใช้งาน หรือพัฒนาประสิทธิภาพซอฟต์แวร์
		รวมทั้งไม่รับรองความถูกต้องหรือประสิทธิภาพการทำงานของซอฟต์แวร์ ตลอดจนไม่รับประกันความเสียหายต่าง
		ๆ อันเกิดจากการใช้ซอฟต์แวร์นี้ทั้งสิ้น
	</p>

